// Height is distance from the top 
platform generatePlatform(int height) {
    platform result;

    result.y_pos = height;

    // Put the platform at a random x position
    result.x_pos = rand() % (RESOLUTION_X - PLATFORM_WIDTH);
    
    // Random number between 0 and 100
    int randNum = rand() % (100 + 1);

    // Use randNum to determine if platform is shown or not.
    if (randNum < 30) {
        result.visible = false;
		result.color = BACKGROUND;
    }
    else {
        result.visible = true;
    }

    randNum = rand() % (100 + 1);
    // Randomly determine platform type.
    // Greater odds on becoming a regular old platform
    if (randNum < 50) {
        result.type = REGULAR;
        result.color = GREEN;
		result.delta_x = 0;
    }
    else if (randNum < 60) {
        result.type = BROKEN;
        result.color = BROWN;
		result.delta_x = 0;
    }
    else if (randNum < 80) {
        result.type = MOVING;
        result.color = BLUE;
		result.delta_x = 1 - 2*(randNum % 2);
    }
	else if (randNum < 90) {
		result.type = SPRING;
		result.color = ORANGE;
		result.delta_x = 0;
	}
    else {
        result.type = DISAPPEARING;
        result.color = WHITE;
		result.delta_x = 0;
    }

	randNum = rand() % (100 + 1);
    if (randNum < 80) {
        result.hasEnemy = false;
    }
    else {
        result.hasEnemy = true;
        result.enemy.visible = true;
        result.enemy.x_pos = result.x_pos + (PLATFORM_WIDTH / 2) - (ENEMY_WIDTH / 2);
        result.enemy.y_pos = result.y_pos - ENEMY_HEIGHT;
    }
	
	//Set the previous positions
	
	result.x_pos_prev = 0;
	result.y_pos_prev = 0;
	result.x_pos_prev2 = 0;
	result.y_pos_prev2 = 0;

    return result;
}

